mood_multipliers = {
    "좋음": {"focus": 1.2, "break": 0.8, "rounds": 1.2},
    "무난함": {"focus": 1.0, "break": 1.0, "rounds": 1.0},
    "우울": {"focus": 0.6, "break": 1.2, "rounds": 0.8},
    "짜증/지침": {"focus": 0.4, "break": 1.5, "rounds": 0.5},
}

def get_adjusted_times(base_focus, base_break, base_rounds, mood):
    multipliers = mood_multipliers.get(mood, {"focus":1.0, "break":1.0, "rounds":1.0})
    adjusted_focus = max(1, round(base_focus * multipliers["focus"]))
    adjusted_break = max(1, round(base_break * multipliers["break"]))
    adjusted_rounds = max(1, round(base_rounds * multipliers["rounds"]))
    return adjusted_focus, adjusted_break, adjusted_rounds

